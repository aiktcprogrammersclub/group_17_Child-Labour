<h2 style="text-align:center">Story of a 13 year-old Delhi girl | Stop Child Labour</h2>
</div>
</br></br></br></br>
<p>Delhi, India. A 13-year-old girl employed as a domestic worker by a couple who are doctors is locked up in their house while her employers are on holiday in
 Thailand. Neighbours hear her frantic screams for help. Her story then makes the national and international headlines.

The "child maid" story shone a light on to the dark recesses of child labour, and the response has been rightly one of outrage. It is heartening to note that
 the centre of the fury is India, and particularly Delhi – the city of the alleged crime. The international media, however, have also questioned whether the 
 Indian mindset enables and perpetuates the mentality of having "servants".
</p>
<p>
It was the British, in the days of the Raj, who were known to have a retinue of servants commonly referred to as ayahs (female servants) and bearers 
(male servants). The British Library archives recount: "The ayahs usually had no contract of employment, despite offering a crucial service to their
 employers. Some could even be dismissed with no pay." When the British left India in 1947, it was predominantly rural and disproportionately poor.
 The Indian elite were joined by the middle classes, and together they perpetuated their erstwhile colonialists' tradition of sourcing household help
 from the much poorer majority, and in a derogative manner. Sadly this practice of having servants that has travelled into coeval times has not precluded
 the employment of children.
</p>
<p>
Childhood should be a time of growth, discovery and learning Child labour is a reprehensible practice that steals childhoods. Yet the plight of these 
children does not always end with their rescue – they can be saved from the frying pan only to be fed to the fire.
</p>
<p>
I recall when Danny Boyle's Slumdog Millionaire was released. As someone working with street and slum children in India I was repeatedly asked "Is is 
really that bad?" Temperatures usually upwards of 33C, in most instances unmitigated by even a fan; being feasted on by myriad disease carrying mosquitoes;
 the stench of dog and human excrement; urine trickling through open alleyways – sadly, they are all part of the slum ground reality, a reality 
 that cannot be communicated by a film.
</p>
<p>
These are the conditions in which millions of childhoods are being played out, and where their adolescence, parenthood and old age will be shaped. 
This is likely to happen because entrenched penury and illiteracy, unrelieved by the system in which they find themselves, becomes self-perpetuating.

It is paramount that we do not treat the symptom of domestic child labour without engineering measures that address the underlying causes. 
Adopting a multi-pronged problem-solving approach is required. This story provides a window to challenge antiquated notions that are antithetical 
to the idea of small families, by establishing family planning workshops, for example.
</p>
<p>
Furthermore, this should be incentivised by the provision of education and/or vocational-based skills training for each child. The argument against 
espousing a family planning approach, despite large families being commonplace among India's poorest quarters, has been that it is politically unpalatable.
</p>
<p>
 However, the combination of the right information complemented by the right rewards will combat child labour and, in the longer run, poverty. The question 
 of political viability then actually lies in the effective delivery of the measures.

A society is judged best by how it treats its marginalised, its young and its old. In the case of India, more than half the population is under the age of 25.
 The time is ripe for India to use its current fury to usher in measures to properly protect its young.
</p>
